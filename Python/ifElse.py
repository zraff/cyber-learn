age = int(input("What's your age? "))

if( age <= 5 and age >= 0 ):
    print("You are a kid")
elif( age <= 15 and age >= 0 ):
    print("You are a big kid")
elif( age <= 21 and age >= 0 ):
    print("You are a bigger kid")
elif( age <= 150 and age >= 0 ):
    print("You are the biggest kid")
else:
    print("Are you a vampire")

